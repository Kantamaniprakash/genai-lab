"""Render the multi-seed robustness check as a markdown summary.

    python -m experiments.summarize_seeds --dataset dev-v1.1 --retriever bm25

writes ``results/summary_<dataset>_<retriever>_seeds.md`` and prints it.

Every grid so far samples questions with one seed (50 per document, seed 0).
The question this summary answers: are the headline effects properties of the
question *population*, or of that one sample? Each seed draws an independent
50-per-document sample, so agreement across seeds is a direct check of
sampling robustness — the cheapest one available before any claim goes into
the writeup.

Statistical care: paired comparisons are valid *within* a seed (same question
set on both sides) and meaningless *across* seeds, so the tables report, per
seed, the within-seed means and within-seed paired deltas. Across-seed spread
of those quantities is what the reader should eyeball: if the paired CI for a
claim excludes zero under every seed, the claim does not ride on the draw.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from experiments.aggregate import (
    BASELINE_SIZES,
    STRUCTURAL_CHUNKERS,
    RunResult,
    check_aligned,
    diff_ci,
    load_raw,
    mean,
)
from experiments.summarize import _table, fmt_diff

ROOT = Path(__file__).resolve().parent.parent

# The claims worth defending across seeds, as (challenger, baseline) labels.
# Ordering matters for sign: positive delta = challenger wins.
HEADLINE_PAIRS = (
    ("fixed-64", "fixed-256"),
    ("fixed-64", "fixed-512"),
    ("sentence-64", "fixed-64"),
    ("sentence-128", "fixed-128"),
)


def _load_seed_grids(
    raw_dir: Path,
    dataset: str,
    retriever: str,
    seeds: list[int],
    sizes: tuple[int, ...] | None = None,
    chunkers: tuple[str, ...] | None = None,
) -> dict[int, list[RunResult]]:
    grids: dict[int, list[RunResult]] = {}
    for seed in seeds:
        runs = load_raw(
            raw_dir,
            dataset=dataset,
            retriever=retriever,
            budget_rule="stop",
            overlap=0,
            seed=seed,
            sizes=sizes,
            chunkers=chunkers,
        )
        if not runs:
            raise SystemExit(
                f"no baseline-grid results for seed {seed} on "
                f"{dataset}/{retriever} in {raw_dir}"
            )
        check_aligned(runs)
        grids[seed] = runs
    labels = [tuple(rr.label for rr in runs) for runs in grids.values()]
    if len(set(labels)) != 1:
        raise SystemExit(f"seeds cover different grids: {labels}")
    return grids


def render_seeds(
    dataset: str,
    retriever: str,
    seeds: list[int],
    raw_dir: Path,
    budget: int = 400,
    sizes: tuple[int, ...] | None = None,
    chunkers: tuple[str, ...] | None = None,
) -> str:
    grids = _load_seed_grids(
        raw_dir, dataset, retriever, seeds, sizes=sizes, chunkers=chunkers
    )
    first = grids[seeds[0]]
    labels = [rr.label for rr in first]
    cfg = first[0].config
    budgets = [int(b) for b in cfg["budgets"]]
    if budget not in budgets:
        raise SystemExit(f"budget {budget} not among run budgets {budgets}")
    n_questions = {seed: len(grids[seed][0].records) for seed in seeds}

    lines = [
        f"# Multi-seed robustness — {dataset}, {retriever}",
        "",
        f"Seeds: {', '.join(str(s) for s in seeds)}; each draws an independent "
        f"{cfg['per_doc_cap']}-per-document question sample "
        f"({', '.join(str(n_questions[s]) for s in seeds)} questions). Paired "
        "bootstrap deltas (10,000 resamples) are computed within each seed; "
        "across-seed spread of the means shows the sampling variability of "
        "the estimates themselves.",
        "",
        "Generated by `python -m experiments.summarize_seeds` from "
        "`results/raw/` — do not edit by hand.",
        "",
        f"## SpanRecall@{budget} (mean) by seed",
        "",
    ]
    rows = []
    for label in labels:
        means = [
            mean(next(rr for rr in grids[s] if rr.label == label).metric("recall", budget))
            for s in seeds
        ]
        rows.append(
            [label, *(f"{m:.3f}" for m in means), f"{max(means) - min(means):.3f}"]
        )
    lines += _table(["config", *(f"seed {s}" for s in seeds), "max−min"], rows)

    lines += [
        f"## Headline paired deltas at B={budget}, per seed "
        "(bold = CI excludes 0)",
        "",
    ]
    by_seed_label = {
        seed: {rr.label: rr for rr in runs} for seed, runs in grids.items()
    }
    rows = []
    for challenger, baseline in HEADLINE_PAIRS:
        if challenger not in by_seed_label[seeds[0]] or baseline not in by_seed_label[seeds[0]]:
            continue
        cells = []
        for seed in seeds:
            runs = by_seed_label[seed]
            cells.append(
                fmt_diff(
                    diff_ci(
                        runs[challenger].metric("recall", budget),
                        runs[baseline].metric("recall", budget),
                    )
                )
            )
        rows.append([f"{challenger} − {baseline}", *cells])
    lines += _table(["comparison", *(f"seed {s}" for s in seeds)], rows)
    return "\n".join(lines)


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize the baseline grid across question-sampling seeds.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--dataset", default="dev-v1.1")
    parser.add_argument("--retriever", default="bm25")
    parser.add_argument("--seeds", nargs="+", type=int, default=[0, 1, 2])
    parser.add_argument("--budget", type=int, default=400)
    parser.add_argument("--raw-dir", type=Path, default=ROOT / "results" / "raw")
    parser.add_argument("--out-dir", type=Path, default=ROOT / "results")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = parse_args(argv)
    text = render_seeds(
        args.dataset,
        args.retriever,
        args.seeds,
        args.raw_dir,
        args.budget,
        sizes=BASELINE_SIZES,
        chunkers=STRUCTURAL_CHUNKERS,
    )
    out = args.out_dir / f"summary_{args.dataset}_{args.retriever}_seeds.md"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(text + "\n", encoding="utf-8")
    print(text)
    print(f"\n[written to {out.relative_to(ROOT)}]")


if __name__ == "__main__":
    main()
