"""Render the cross-retriever comparison as a markdown summary.

    python -m experiments.summarize_retrievers --dataset dev-v1.1

writes ``results/summary_<dataset>_retrievers.md`` and prints it.

All retrievers rank the *same* chunk lists for the *same* questions (chunking
is retriever-independent and question sampling is seeded), so per-question
paired comparisons across retrievers are valid at every grid point. The
interesting questions this summary answers:

- Does the budget-matched size ordering (small chunks win) hold per retriever,
  or is it a BM25 artifact?
- How large is the retriever effect relative to the chunking effect, at
  matched configuration?
- Where does LSA's latent bottleneck actually bind? Per-document chunk
  collections cap the SVD rank at n_chunks − 1, so large-chunk configs give
  LSA (nearly) full rank — where it degenerates toward plain TF-IDF. The
  realized-rank table makes that explicit instead of leaving it to folklore.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from experiments.aggregate import (
    BASELINE_SIZES,
    STRUCTURAL_CHUNKERS,
    RunResult,
    check_aligned,
    diff_ci,
    load_raw,
    mean,
)
from experiments.summarize import _table, fmt_diff

ROOT = Path(__file__).resolve().parent.parent


def _load_by_retriever(
    raw_dir: Path,
    dataset: str,
    retrievers: list[str],
    seed: int = 0,
    sizes: tuple[int, ...] | None = None,
    chunkers: tuple[str, ...] | None = None,
) -> dict[str, list[RunResult]]:
    by_retriever: dict[str, list[RunResult]] = {}
    for retriever in retrievers:
        runs = load_raw(
            raw_dir,
            dataset=dataset,
            retriever=retriever,
            budget_rule="stop",
            overlap=0,
            seed=seed,
            sizes=sizes,
            chunkers=chunkers,
        )
        if not runs:
            raise SystemExit(
                f"no baseline-grid results for retriever {retriever!r} on "
                f"{dataset} in {raw_dir}"
            )
        by_retriever[retriever] = runs
    labels = [tuple(rr.label for rr in runs) for runs in by_retriever.values()]
    if len(set(labels)) != 1:
        raise SystemExit(f"retrievers cover different grids: {labels}")
    check_aligned([rr for runs in by_retriever.values() for rr in runs])
    return by_retriever


def render_retrievers(
    dataset: str,
    retrievers: list[str],
    raw_dir: Path,
    reference: str = "bm25",
    seed: int = 0,
    sizes: tuple[int, ...] | None = None,
    chunkers: tuple[str, ...] | None = None,
) -> str:
    by_retriever = _load_by_retriever(
        raw_dir, dataset, retrievers, seed=seed, sizes=sizes, chunkers=chunkers
    )
    if reference not in by_retriever:
        raise SystemExit(f"reference retriever {reference!r} not among {retrievers}")
    labels = [rr.label for rr in by_retriever[reference]]
    cfg = by_retriever[reference][0].config
    budgets = [int(b) for b in cfg["budgets"]]
    hit_ks = [int(k) for k in cfg["hit_ks"]]
    hit_k = 5 if 5 in hit_ks else max(hit_ks)
    n_questions = len(by_retriever[reference][0].records)
    challengers = [r for r in retrievers if r != reference]

    def runs_at(label: str) -> dict[str, RunResult]:
        return {
            retriever: next(rr for rr in runs if rr.label == label)
            for retriever, runs in by_retriever.items()
        }

    lines = [
        f"# Cross-retriever summary — {dataset}",
        "",
        f"Retrievers: {', '.join(retrievers)}. {n_questions} questions "
        f"({cfg['per_doc_cap']}/document cap, seed {cfg['seed']}), zero overlap, "
        "stop-before-exceed budget rule. Chunk lists are identical across "
        "retrievers at each grid point, so all deltas are per-question paired "
        f"(10,000 bootstrap resamples) against **{reference}**.",
        "",
        "Generated by `python -m experiments.summarize_retrievers` from "
        "`results/raw/` — do not edit by hand.",
        "",
    ]

    for budget in budgets:
        lines += [f"## SpanRecall@{budget} (mean) by retriever", ""]
        lines += _table(
            ["config", *retrievers],
            [
                [
                    label,
                    *(
                        f"{mean(rr.metric('recall', budget)):.3f}"
                        for rr in runs_at(label).values()
                    ),
                ]
                for label in labels
            ],
        )

    for challenger in challengers:
        lines += [
            f"## ΔSpanRecall, {challenger} − {reference} "
            "(paired per config; bold = CI excludes 0)",
            "",
        ]
        lines += _table(
            ["config", *(f"B={b}" for b in budgets)],
            [
                [
                    label,
                    *(
                        fmt_diff(
                            diff_ci(
                                runs_at(label)[challenger].metric("recall", b),
                                runs_at(label)[reference].metric("recall", b),
                            )
                        )
                        for b in budgets
                    ),
                ]
                for label in labels
            ],
        )

    lines += [f"## hit@{hit_k} (mean) by retriever", ""]
    lines += _table(
        ["config", *retrievers],
        [
            [
                label,
                *(f"{mean(rr.hits(hit_k)):.3f}" for rr in runs_at(label).values()),
            ]
            for label in labels
        ],
    )

    rank_rows = []
    for label in labels:
        for retriever, rr in runs_at(label).items():
            stats = rr.retriever_stats
            if stats is None or "n_components" not in stats:
                continue
            rank_rows.append(
                [
                    f"{label} ({retriever})",
                    str(stats["n_components"]),
                    str(stats["realized_rank_min"]),
                    str(stats["realized_rank_median"]),
                    str(stats["realized_rank_max"]),
                    f"{stats['n_docs_data_bounded']}/{stats['n_docs']}",
                ]
            )
    if rank_rows:
        lines += [
            "## LSA realized latent rank per document index",
            "",
            "The SVD rank is min(n_components, n_chunks − 1, n_terms − 1) per "
            "document. \"data-bounded\" counts documents where the chunk count "
            "(not n_components) set the rank — there the decomposition is "
            "(nearly) full-rank and LSA approaches plain TF-IDF.",
            "",
        ]
        lines += _table(
            ["config", "n_components", "rank min", "median", "max", "data-bounded"],
            rank_rows,
        )

    truncation_rows = []
    for label in labels:
        for retriever, rr in runs_at(label).items():
            stats = rr.retriever_stats
            if stats is None or "max_seq_length" not in stats:
                continue
            frac = stats["n_chunks_truncated"] / stats["n_chunks"]
            truncation_rows.append(
                [
                    f"{label} ({retriever})",
                    stats["model"],
                    str(stats["max_seq_length"]),
                    f"{stats['n_chunks_truncated']}/{stats['n_chunks']}",
                    f"{frac:.1%}",
                ]
            )
    if truncation_rows:
        lines += [
            "## Dense encoder truncation exposure",
            "",
            "The encoder silently truncates inputs at its maximum sequence "
            "length (wordpieces, specials included), so for chunks past that "
            "window the dense retriever scores a prefix. Any dense result on "
            "configs with high truncation exposure measures prefix retrieval, "
            "not whole-chunk retrieval — that is the deployment reality of "
            "pairing large chunks with a small encoder, and the exposure "
            "column quantifies how much of the grid it affects.",
            "",
        ]
        lines += _table(
            ["config", "model", "max tokens", "chunks truncated", "share"],
            truncation_rows,
        )
    return "\n".join(lines)


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize the baseline grid across retrievers.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--dataset", default="dev-v1.1")
    parser.add_argument(
        "--retrievers", nargs="+", default=["bm25", "tfidf", "lsa", "dense"]
    )
    parser.add_argument("--reference", default="bm25")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--raw-dir", type=Path, default=ROOT / "results" / "raw")
    parser.add_argument("--out-dir", type=Path, default=ROOT / "results")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = parse_args(argv)
    text = render_retrievers(
        args.dataset,
        args.retrievers,
        args.raw_dir,
        args.reference,
        seed=args.seed,
        sizes=BASELINE_SIZES,
        chunkers=STRUCTURAL_CHUNKERS,
    )
    out = args.out_dir / f"summary_{args.dataset}_retrievers.md"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(text + "\n", encoding="utf-8")
    print(text)
    print(f"\n[written to {out.relative_to(ROOT)}]")


if __name__ == "__main__":
    main()
