"""Render raw grid results as a markdown summary with paired bootstrap CIs.

    python -m experiments.summarize --dataset dev-v1.1 --retriever bm25

writes ``results/summary_<dataset>_<retriever>.md`` and prints it. The
summary carries the baseline grid (zero overlap, stop budget rule); the
overlap and budget-rule ablations are rendered by
``experiments.summarize_ablations``. The README quotes a curated subset of
these tables (never numbers from anywhere else).
"""

from __future__ import annotations

import argparse
from pathlib import Path

from experiments.aggregate import (
    RunResult,
    check_aligned,
    diff_ci,
    load_raw,
    mean,
)
from src.metrics import BootstrapResult

ROOT = Path(__file__).resolve().parent.parent

SPAN_METRICS = (("recall", "SpanRecall"), ("precision", "SpanPrecision"), ("iou", "SpanIoU"))


def find_baseline(results: list[RunResult], label: str) -> RunResult:
    for rr in results:
        if rr.label == label:
            return rr
    raise ValueError(f"baseline {label!r} not found among {[r.label for r in results]}")


def fmt_diff(res: BootstrapResult) -> str:
    cell = f"{res.mean_diff:+.3f} [{res.ci_low:+.3f}, {res.ci_high:+.3f}]"
    return f"**{cell}**" if res.significant else cell


def _table(header: list[str], rows: list[list[str]]) -> list[str]:
    lines = ["| " + " | ".join(header) + " |", "|" + "---|" * len(header)]
    lines += ["| " + " | ".join(row) + " |" for row in rows]
    lines.append("")
    return lines


def pairwise_same_size_rows(
    results: list[RunResult], budgets: list[int], metric: str = "recall"
) -> list[list[str]]:
    """Δmetric rows for sentence-vs-fixed and recursive-vs-fixed at each size.

    Same-size comparisons isolate the chunk-boundary policy from the size
    effect; only zero-overlap stop-rule runs qualify (one run per
    (chunker, size) key, same protocol on both sides).
    """
    by = {
        (rr.config["chunker"], rr.config["chunk_size"]): rr
        for rr in results
        if rr.config["overlap"] == 0 and rr.config["budget_rule"] == "stop"
    }
    rows = []
    for challenger in ("sentence", "recursive"):
        for size in sorted({size for _, size in by}):
            if (challenger, size) not in by or ("fixed", size) not in by:
                continue
            a, b = by[(challenger, size)], by[("fixed", size)]
            rows.append(
                [
                    f"{a.label} vs {b.label}",
                    *(
                        fmt_diff(diff_ci(a.metric(metric, bud), b.metric(metric, bud)))
                        for bud in budgets
                    ),
                ]
            )
    return rows


def render_summary(results: list[RunResult], baseline_label: str) -> str:
    check_aligned(results)
    baseline = find_baseline(results, baseline_label)
    cfg = results[0].config
    budgets = [int(b) for b in cfg["budgets"]]
    hit_ks = [int(k) for k in cfg["hit_ks"]]
    n_questions = len(results[0].records)
    budget_cols = [f"B={b}" for b in budgets]

    lines = [
        f"# Grid summary — {cfg['dataset']}, {cfg['retriever']}",
        "",
        f"{n_questions} questions ({cfg['per_doc_cap']}/document cap, seed "
        f"{cfg['seed']}), budgets in regex word tokens, budget rule "
        f"stop-before-exceed. Paired comparisons use 10,000 bootstrap "
        f"resamples over questions; baseline: **{baseline_label}**.",
        "",
        "Generated by `python -m experiments.summarize` from `results/raw/` — "
        "do not edit by hand.",
        "",
        "## Chunk statistics",
        "",
    ]
    lines += _table(
        ["config", "chunks", "tokens/chunk median", "mean", "max"],
        [
            [
                rr.label,
                str(rr.chunk_stats["n_chunks"]),
                str(rr.chunk_stats["tokens_median"]),
                f"{rr.chunk_stats['tokens_mean']:.1f}",
                str(rr.chunk_stats["tokens_max"]),
            ]
            for rr in results
        ],
    )

    for key, title in SPAN_METRICS:
        lines += [f"## {title}@B (mean)", ""]
        lines += _table(
            ["config", *budget_cols],
            [
                [rr.label, *(f"{mean(rr.metric(key, b)):.3f}" for b in budgets)]
                for rr in results
            ],
        )

    lines += [
        f"## ΔSpanRecall vs {baseline_label} (mean [95% CI]; bold = CI excludes 0)",
        "",
    ]
    diff_rows = []
    for rr in results:
        if rr is baseline:
            diff_rows.append([rr.label, *(["—"] * len(budgets))])
        else:
            diff_rows.append(
                [
                    rr.label,
                    *(
                        fmt_diff(diff_ci(rr.metric("recall", b), baseline.metric("recall", b)))
                        for b in budgets
                    ),
                ]
            )
    lines += _table(["config", *budget_cols], diff_rows)

    pairwise = pairwise_same_size_rows(results, budgets)
    if pairwise:
        lines += [
            "## Same-size pairwise ΔSpanRecall (chunk-boundary policy, size held fixed)",
            "",
        ]
        lines += _table(["comparison", *budget_cols], pairwise)

    lines += ["## hit@k (fraction of questions with any gold overlap in top-k)", ""]
    lines += _table(
        ["config", *(f"k={k}" for k in hit_ks)],
        [
            [rr.label, *(f"{mean(rr.hits(k)):.3f}" for k in hit_ks)]
            for rr in results
        ],
    )

    lines += [
        "## Budget utilization (mean prompt tokens spent / budget)",
        "",
        "Utilization far below 1.0 means stop-before-exceed left most of the "
        "budget unspent — the config's chunks are too large for the budget.",
        "",
    ]
    lines += _table(
        ["config", *budget_cols],
        [
            [
                rr.label,
                *(f"{mean([float(t) for t in rr.tokens_used(b)]) / b:.2f}" for b in budgets),
            ]
            for rr in results
        ],
    )
    return "\n".join(lines)


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize raw grid results.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--dataset", default="dev-v1.1")
    parser.add_argument("--retriever", default="bm25")
    parser.add_argument("--baseline", default="fixed-256")
    parser.add_argument("--raw-dir", type=Path, default=ROOT / "results" / "raw")
    parser.add_argument("--out-dir", type=Path, default=ROOT / "results")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = parse_args(argv)
    results = load_raw(
        args.raw_dir,
        dataset=args.dataset,
        retriever=args.retriever,
        budget_rule="stop",
        overlap=0,
    )
    if not results:
        raise SystemExit(f"no results for {args.dataset}/{args.retriever} in {args.raw_dir}")
    text = render_summary(results, args.baseline)
    out = args.out_dir / f"summary_{args.dataset}_{args.retriever}.md"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(text + "\n", encoding="utf-8")
    print(text)
    print(f"\n[written to {out.relative_to(ROOT)}]")


if __name__ == "__main__":
    main()
