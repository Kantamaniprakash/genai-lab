"""Render the semantic-chunker comparison as a markdown summary.

    python -m experiments.summarize_semantic --dataset dev-v1.1 --retriever bm25

writes ``results/summary_<dataset>_<retriever>_semantic.md`` and prints it.

The question this summary answers: once retrieval is budget-matched, do
embedding breakpoints beat the regex sentence boundaries they refine? The
semantic chunker packs the *same* sentences under the *same* budget as
``SentenceChunker`` and differs only in refusing to pack across an
embedding breakpoint — so ``semantic − sentence`` at matched nominal size
isolates the value of the breakpoints themselves, and ``semantic − fixed``
gives the family the same cross-family comparison every other chunker got.

Two readings the tables force explicitly, because the comparison is easy to
get wrong:

- **Realized size.** Breakpoints can only shorten chunks, so a semantic
  configuration operates at a smaller realized size than its nominal budget;
  the chunk-statistics table puts realized means side by side so a size
  effect is never misread as a boundary effect.
- **Degeneracy.** If the percentile threshold never fires, the semantic
  chunker IS the sentence chunker; the breakpoint table (from persisted
  ``chunker_stats``) shows the realized breakpoint rate per configuration.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from experiments.aggregate import RunResult, check_aligned, diff_ci, load_raw, mean
from experiments.summarize import _table, fmt_diff

ROOT = Path(__file__).resolve().parent.parent

FAMILIES = ("fixed", "sentence", "semantic")


def _by_config(results: list[RunResult]) -> dict[tuple[str, int], RunResult]:
    return {
        (rr.config["chunker"], rr.config["chunk_size"]): rr
        for rr in results
        if rr.config["chunker"] in FAMILIES
    }


def _delta_rows(
    by: dict[tuple[str, int], RunResult],
    challenger: str,
    baseline: str,
    budgets: list[int],
    metric: str,
) -> list[list[str]]:
    rows = []
    for size in sorted({size for _, size in by}):
        if (challenger, size) not in by or (baseline, size) not in by:
            continue
        a, b = by[(challenger, size)], by[(baseline, size)]
        rows.append(
            [
                str(size),
                *(
                    fmt_diff(diff_ci(a.metric(metric, bud), b.metric(metric, bud)))
                    for bud in budgets
                ),
            ]
        )
    return rows


def _hit_delta_rows(
    by: dict[tuple[str, int], RunResult],
    challenger: str,
    baseline: str,
    hit_ks: list[int],
) -> list[list[str]]:
    rows = []
    for size in sorted({size for _, size in by}):
        if (challenger, size) not in by or (baseline, size) not in by:
            continue
        a, b = by[(challenger, size)], by[(baseline, size)]
        rows.append(
            [
                str(size),
                *(fmt_diff(diff_ci(a.hits(k), b.hits(k))) for k in hit_ks),
            ]
        )
    return rows


def render_semantic(results: list[RunResult]) -> str:
    """Markdown comparison of the semantic family against sentence and fixed.

    ``results`` must be aligned zero-overlap stop-rule runs containing at
    least one semantic configuration and its matched-size counterparts.
    """
    check_aligned(results)
    by = _by_config(results)
    semantic = [rr for rr in results if rr.config["chunker"] == "semantic"]
    if not semantic:
        raise ValueError("no semantic-chunker runs among the results")
    cfg = results[0].config
    budgets = [int(b) for b in cfg["budgets"]]
    hit_ks = [int(k) for k in cfg["hit_ks"]]
    budget_cols = [f"B={b}" for b in budgets]
    n_questions = len(results[0].records)

    lines = [
        f"# Semantic chunker comparison — {cfg['dataset']}, {cfg['retriever']}",
        "",
        f"{n_questions} questions, budgets in regex word tokens, budget rule "
        "stop-before-exceed, zero overlap everywhere. Paired comparisons use "
        "10,000 bootstrap resamples over questions; bold = 95% CI excludes 0. "
        "`semantic − sentence` at matched nominal size isolates the embedding "
        "breakpoints: both sides pack the same sentences under the same "
        "budget, and the semantic side additionally refuses to pack across a "
        "breakpoint.",
        "",
        "Generated by `python -m experiments.summarize_semantic` from "
        "`results/raw/` — do not edit by hand.",
        "",
        "## Chunk statistics (breakpoints only shorten: compare realized means)",
        "",
    ]
    ordered = [
        by[(family, size)]
        for size in sorted({size for _, size in by})
        for family in FAMILIES
        if (family, size) in by
    ]
    lines += _table(
        ["config", "chunks", "tokens/chunk median", "mean", "max"],
        [
            [
                rr.label,
                str(rr.chunk_stats["n_chunks"]),
                str(rr.chunk_stats["tokens_median"]),
                f"{rr.chunk_stats['tokens_mean']:.1f}",
                str(rr.chunk_stats["tokens_max"]),
            ]
            for rr in ordered
        ],
    )

    lines += [
        "## Breakpoint statistics (persisted `chunker_stats`; "
        "identical segmentation across sizes by construction)",
        "",
    ]
    rows = []
    for rr in semantic:
        stats = rr.chunker_stats
        if stats is None:
            continue
        rate = stats["n_breakpoints"] / stats["n_gaps"] if stats["n_gaps"] else 0.0
        rows.append(
            [
                rr.label,
                str(stats["n_sentences"]),
                str(stats["n_breakpoints"]),
                f"{rate:.3f}",
                str(stats["sentences_over_window"]),
                f"{stats['encoder']} @ p{stats['percentile']:g}",
            ]
        )
    lines += _table(
        ["config", "sentences", "breakpoints", "rate/gap", "over window", "encoder"],
        rows,
    )

    lines += ["## SpanRecall@B (mean)", ""]
    lines += _table(
        ["config", *budget_cols],
        [
            [rr.label, *(f"{mean(rr.metric('recall', b)):.3f}" for b in budgets)]
            for rr in ordered
        ],
    )

    lines += [
        "## ΔSpanRecall: semantic − sentence at matched nominal size",
        "",
    ]
    lines += _table(
        ["size", *budget_cols], _delta_rows(by, "semantic", "sentence", budgets, "recall")
    )

    lines += [
        "## ΔSpanRecall: semantic − fixed at matched nominal size",
        "",
    ]
    lines += _table(
        ["size", *budget_cols], _delta_rows(by, "semantic", "fixed", budgets, "recall")
    )

    lines += [
        "## Δhit@k: semantic − sentence at matched nominal size",
        "",
    ]
    lines += _table(
        ["size", *(f"k={k}" for k in hit_ks)],
        _hit_delta_rows(by, "semantic", "sentence", hit_ks),
    )
    return "\n".join(lines)


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize the semantic-chunker comparison.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--dataset", default="dev-v1.1")
    parser.add_argument("--retriever", default="bm25")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--raw-dir", type=Path, default=ROOT / "results" / "raw")
    parser.add_argument("--out-dir", type=Path, default=ROOT / "results")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = parse_args(argv)
    results = load_raw(
        args.raw_dir,
        dataset=args.dataset,
        retriever=args.retriever,
        budget_rule="stop",
        overlap=0,
        seed=args.seed,
    )
    if not results:
        raise SystemExit(f"no results for {args.dataset}/{args.retriever} in {args.raw_dir}")
    text = render_semantic(results)
    out = args.out_dir / f"summary_{args.dataset}_{args.retriever}_semantic.md"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(text + "\n", encoding="utf-8")
    print(text)
    print(f"\n[written to {out.relative_to(ROOT)}]")


if __name__ == "__main__":
    main()
